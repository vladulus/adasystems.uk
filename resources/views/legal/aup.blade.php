<h2>1. Introduction</h2>
<p>This Acceptable Use Policy ("AUP") governs your use of ADASystems fleet management platform, Devices, and related services. By using our services, you agree to comply with this policy.</p>

<h2>2. Permitted Use</h2>
<p>You may use our services only for:</p>
<ul>
    <li>Lawful fleet management and vehicle tracking purposes</li>
    <li>Monitoring vehicles owned or operated by your organisation</li>
    <li>Compliance with transport regulations and tachograph requirements</li>
    <li>Driver safety and performance management</li>
    <li>Business analytics and operational optimisation</li>
</ul>

<h2>3. Prohibited Activities</h2>
<p>You must NOT use our services to:</p>
<ul>
    <li>Track vehicles or individuals without proper authorisation or consent</li>
    <li>Engage in illegal surveillance or stalking</li>
    <li>Violate any applicable laws, regulations, or third-party rights</li>
    <li>Attempt to gain unauthorised access to our systems or other users' accounts</li>
    <li>Reverse engineer, decompile, or disassemble our software or Devices</li>
    <li>Modify, tamper with, or disable Device hardware or software</li>
    <li>Use automated scripts or bots to access the platform without authorisation</li>
    <li>Transmit malware, viruses, or harmful code</li>
    <li>Interfere with or disrupt the integrity or performance of our services</li>
    <li>Share login credentials with unauthorised parties</li>
    <li>Use the service for competitive intelligence against ADASystems</li>
    <li>Resell, sublicense, or redistribute our services without written consent</li>
</ul>

<h2>4. Device Restrictions</h2>
<p>Regarding ADASystems Devices, you must NOT:</p>
<ul>
    <li>Open, modify, or physically tamper with Device hardware</li>
    <li>Install third-party software or firmware on the Device</li>
    <li>Attempt to bypass security measures or administrative controls</li>
    <li>Use the Device with any platform other than ADASystems</li>
    <li>Remove or obscure Device identification labels</li>
    <li>Transfer the Device to third parties without ADASystems approval</li>
</ul>

<h2>5. User Responsibilities</h2>
<p>As a user, you are responsible for:</p>
<ul>
    <li>Maintaining the confidentiality of your account credentials</li>
    <li>Ensuring all users under your account comply with this AUP</li>
    <li>Notifying ADASystems immediately of any security breaches</li>
    <li>Ensuring proper consent from drivers being monitored</li>
    <li>Complying with employment laws regarding employee monitoring</li>
    <li>Using data collected through the platform in compliance with GDPR</li>
</ul>

<h2>6. Driver Notification Requirements</h2>
<p>If you are monitoring employees or contractors using our services, you must inform them about the tracking, what data is collected, how it will be used, who has access to it, and their rights under data protection law. Failure to properly notify drivers may violate employment and privacy laws.</p>

<h2>7. Enforcement</h2>
<p>Violations of this AUP may result in warnings, suspension of service, termination of account, remote deactivation of Devices, or legal action. ADASystems reserves the right to investigate suspected violations and cooperate with law enforcement where required.</p>

<h2>8. Reporting Violations</h2>
